// Basic Math Operations
// Basic arithmetic
const sum = 5 + 3; // 8
const difference = 10 - 4; // 6
const product = 6 * 7; // 42
const quotient = 15 / 3; // 5
const remainder = 17 % 5; // 2
const exponent = 2 ** 3; // 8

// Math object methods
Math.abs(-5); // 5 (absolute value)
Math.ceil(4.3); // 5 (round up)
Math.floor(4.7); // 4 (round down)
Math.round(4.5); // 5 (nearest integer)
Math.max(1, 3, 2); // 3
Math.min(1, 3, 2); // 1

//Advanced Mathematical Functions
// Trigonometric functions (angles in radians)
Math.sin(Math.PI / 2); // 1
Math.cos(Math.PI); // -1
Math.tan(Math.PI / 4); // ~1

// Exponential and logarithmic functions
Math.exp(1); // ~2.718 (e^1)
Math.log(Math.E); // 1 (natural log)
Math.log10(100); // 2
Math.log2(8); // 3

// Power functions
Math.pow(2, 3); // 8
Math.sqrt(16); // 4
Math.cbrt(27); // 3
